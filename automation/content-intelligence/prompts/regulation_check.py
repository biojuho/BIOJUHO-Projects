"""2단계 — 플랫폼 규제 & 알고리즘 점검 프롬프트 템플릿."""

REGULATION_CHECK_SYSTEM = """\
너는 소셜 미디어 플랫폼 정책 전문가야.
각 플랫폼의 최신 콘텐츠 정책, 알고리즘 변화, 페널티 리스크를 정확하게 분석해.
추측이 아닌 공식 정책 문서와 최근 사례에 기반한 분석만 제공해."""


def build_regulation_prompt(platform: str, lookback_days: int = 30) -> str:
    """플랫폼별 규제 점검 프롬프트를 생성한다."""

    platform_specifics = {
        "x": {
            "name": "X (구 Twitter)",
            "checks": (
                "- 최근 정책 변경 (커뮤니티 가이드라인, API 정책)\n"
                "- Shadowban 트리거 요소: 해시태그 남용, 외부 링크 과다, "
                "반복 게시, 봇 패턴, 대량 팔로우/언팔\n"
                "- 추천 알고리즘 우대 요소: 인용 RT, 북마크, 체류 시간, "
                "Premium+ 장문 우대, 이미지/영상 첨부\n"
                "- X Premium 유료 사용자 우대 정책 변화"
            ),
        },
        "threads": {
            "name": "Threads (Meta)",
            "checks": (
                "- 콘텐츠 가이드라인 (Meta 정책 기반)\n"
                "- 외부 링크 처리: 도달률 감소 여부, 링크 미리보기 정책\n"
                "- 추천 피드 로직: 댓글 가중치, 공감 반응, 팔로잉 vs 추천\n"
                "- 해시태그 지원 여부 및 효과\n"
                "- 텍스트 vs 이미지/영상 게시 가중치"
            ),
        },
        "naver": {
            "name": "네이버 블로그",
            "checks": (
                "- C-Rank 알고리즘: 전문성 지수, 카테고리 일관성, 원본 콘텐츠 비율\n"
                "- D.I.A. 알고리즘: 사용자 체류 시간, 클릭률, 원본 이미지 포함율\n"
                "- 저품질 판정 기준: 외부 링크 과다, 복붙 콘텐츠, 키워드 스터핑, "
                "짧은 글 반복, 급격한 키워드 변경\n"
                "- 상위노출 조건: 검색 키워드 LSI, 이미지 3장 이상, "
                "1500자 이상, 체류 시간 확보 구조"
            ),
        },
    }

    spec = platform_specifics.get(
        platform,
        {"name": platform, "checks": "- 해당 플랫폼의 최신 정책과 알고리즘"},
    )

    return f"""\
[작업]
{spec['name']}의 최신 콘텐츠 정책과 알고리즘 변화를 점검해줘.

■ 점검 기간: 최근 {lookback_days}일 이내 변화

■ 점검 항목
{spec['checks']}

■ 정리할 항목
1. 최근 변경된 규제 또는 정책 (최근 {lookback_days}일 이내)
2. 콘텐츠 제한/페널티 유발 요소 (금지어, 외부링크, 반복 게시 등)
3. 알고리즘이 우대하는 콘텐츠 특성
4. 실무 체크리스트: "이것만은 피해라 / 이것은 반드시 해라"

■ 출력 형식 — 반드시 아래 JSON 구조로 응답해줘:
```json
{{
  "platform": "{platform}",
  "policy_changes": [
    {{"date": "YYYY-MM-DD", "change": "변경 내용", "impact": "영향"}}
  ],
  "penalty_triggers": [
    "페널티 유발 요소 1",
    "페널티 유발 요소 2"
  ],
  "algorithm_preferences": [
    "우대 요소 1",
    "우대 요소 2"
  ],
  "do_list": [
    "반드시 해야 할 것 1",
    "반드시 해야 할 것 2"
  ],
  "dont_list": [
    "절대 하면 안 되는 것 1",
    "절대 하면 안 되는 것 2"
  ]
}}
```

반드시 JSON만 응답하고, 다른 텍스트는 포함하지 마."""
