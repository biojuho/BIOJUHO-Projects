export function createLocaleMock(messages) {
  const t = (key, values = {}) => {
    const entry = messages[key];
    if (typeof entry !== 'string') return entry || key;
    return entry.replace(/\{(\w+)\}/g, (_, token) => String(values[token] ?? ''));
  };

  const localeState = {
    locale: 'en-US',
    outputLanguage: 'en',
    setLocale: () => {},
    toggleLocale: () => {},
    t,
  };

  return {
    useLocale: () => localeState,
  };
}
